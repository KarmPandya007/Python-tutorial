# Data Structures

"""
This file covers data structures in Python, including lists, tuples, sets, and dictionaries.
"""

# Lists
my_list = [1, 2, 3, 4, 5]
my_list.append(6)
print(f"List: {my_list}")

# Tuples
my_tuple = (1, 2, 3)
print(f"Tuple: {my_tuple}")

# Sets
my_set = {1, 2, 3}
my_set.add(4)
print(f"Set: {my_set}")

# Dictionaries
my_dict = {"key1": "value1", "key2": "value2"}
my_dict["key3"] = "value3"
print(f"Dictionary: {my_dict}")

